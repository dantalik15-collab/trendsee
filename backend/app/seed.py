async def seed() -> None:
    async with async_session_factory() as session:
        user = User(name=SEED_USER_NAME)
        session.add(user)
        await session.commit()
        await session.refresh(user)
        logger.info("Создан пользователь id=%d name=%s", user.id, user.name)

        for i in range(SEED_PUBLICATIONS_COUNT):
            title, text = SAMPLE_PUBLICATIONS[i % len(SAMPLE_PUBLICATIONS)]
            publication = Publication(
                user_id=user.id,
                title=title,
                text=text,
            )
            session.add(publication)

        await session.commit()
        logger.info("Создано %d публикаций", SEED_PUBLICATIONS_COUNT)


if __name__ == "__main__":
    asyncio.run(seed())
